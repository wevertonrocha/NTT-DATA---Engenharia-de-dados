# Inteiro p/ float
preco = 10
print(preco)
print(type(preco))
#10
preco = float(preco)
print(preco)
print(type(preco))
#10.0
preco = 10 / 2
print(preco)
print(type(preco))


#Float p/ inteiro
preco = 10.3
print(preco)
print(type(preco))

preco = int(preco)
print(preco)
print(type(preco))


#Conversão por divisão
preco = 10
print(preco)
print(type(preco))

print(preco / 2)
print(type(preco))

print(preco // 2)
print(type(preco))

